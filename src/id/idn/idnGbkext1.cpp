SInt convertMbToWc4Gbkext1( void    * aSrc,
                            SInt      aSrcRemain,
                            void    * aDest,
                            SInt   /* aDestRemain */ )
{
/***********************************************************************
 *
 * Description :
 *     PROJ-2414 [] GBK, CP936 character set 
 *     GBKEXT1 ==> UTF16BE
 *
 * Implementation :
 *     1) GBKEXT1   .
 *
 ***********************************************************************/

    UChar  * sSrcCharPtr;
    UInt     sNum;
    SInt     sRet;
    UShort   sWc;

    sSrcCharPtr = (UChar *)aSrc;

    if ( ( sSrcCharPtr[0] >= 0x81 ) && ( sSrcCharPtr[0] <= 0xa0 ) )
    {
        if ( aSrcRemain < 2 )
        {
            sRet = RET_TOOFEW;
        }
        else
        {
            /* 1) GBKEXT1    */
            if ( ( ( sSrcCharPtr[1] >= 0x40 ) && ( sSrcCharPtr[1] < 0x7f ) ) ||
                 ( ( sSrcCharPtr[1] >= 0x80 ) && ( sSrcCharPtr[1] < 0xff ) ) )
            {
                sNum = ( 190 * ( sSrcCharPtr[0] - 0x81 ) ) +
                         ( sSrcCharPtr[1] -
                           ( sSrcCharPtr[1] >= 0x80 ? 0x41 : 0x40 ) );

                if ( sNum < 6080 )
                {
                    sWc = gGbkExt12Uni81[sNum] ^ IDN_GBKEXT1_XOR_VALUE;
                    WC_TO_UTF16BE( aDest, sWc );

                    sRet = 2;
                }
                else
                {
                    /* sNum  6079      .
                     *
                     * sNum = ( 190 * ( 0xa0 - 0x81 ) ) = 5890
                     * sNum += ( 0xfe - 0x41 ) = 189
                     * sNum <= 6079
                     */
                    sRet = RET_ILSEQ;
                }
            }
            else
            {
                sRet = RET_ILSEQ;
            }
        }
    }
    else
    {
        /* GBK    ,     . */
        sRet = RET_ILSEQ;
    }

    return sRet;
}
