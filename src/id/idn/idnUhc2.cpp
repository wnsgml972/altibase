SInt convertMbToWc4Uhc2( void    * aSrc,
                         SInt      aSrcRemain,
                         void    * aDest,
                         SInt      /* aDestRemain */ )
{
/***********************************************************************
 *
 * Description :
 *      CP949 ==> UTF16에서 사용하는 함수
 *
 * Implementation :
 *
 ***********************************************************************/

    UChar   * sSrcCharPtr;
    UInt      i;
    SInt      sRet;
    UInt      row;
    UInt      col;
    UShort    wc;
    UInt      sIndex;

    sSrcCharPtr = (UChar *)aSrc;

    if ((sSrcCharPtr[0] >= 0xa0 && sSrcCharPtr[0] <= 0xc6))
    {
        if (aSrcRemain >= 2)
        {
            if( (sSrcCharPtr[1] >= 0x41 && sSrcCharPtr[1] < 0x5b) || 
                (sSrcCharPtr[1] >= 0x61 && sSrcCharPtr[1] < 0x7b) || 
                (sSrcCharPtr[1] >= 0x81 && sSrcCharPtr[1] < 0xff) )
            {
                row = sSrcCharPtr[0] - 0xa1;
                col = sSrcCharPtr[1] - 
                      (sSrcCharPtr[1] >= 0x81 ? 
                                0x4d : sSrcCharPtr[1] >= 0x61 ? 0x47 : 0x41);
                i = 84 * row + col;

                if (i < 3126)
                {
                    sIndex = 2*row+(col>=42?1:0);
                    if (sIndex >= 76)
                    {
                        sRet = RET_ILSEQ;
                    }
                    else
                    {
                        wc = (UShort) ( (uhc_2_2uni_main_pagea1[sIndex] ^ UHC2_XOR_VALUE) +
                                        (uhc_2_2uni_pagea1[i] ^ UHC2_XOR_VALUE) );

                        WC_TO_UTF16BE( aDest, wc );
                    
                        sRet = 2;
                    }
                }
                else
                {
                    sRet = RET_ILSEQ;
                }
            }
            else
            {
                sRet = RET_ILSEQ;
            }
        }
        else
        {
            sRet = RET_TOOFEW;
        }
    }
    else
    {
        sRet = RET_ILSEQ;
    }

    return sRet;
}

SInt convertWcToMb4Uhc2( void    * aSrc,
                         SInt      /* aSrcRemain */,
                         void    * aDest,
                         SInt      aDestRemain )
{
/***********************************************************************
 *
 * Description :
 *      CP949 <== UTF16BE에서 사용하는 함수
 *
 * Implementation :
 *
 ***********************************************************************/

    const Summary16   * sSummary = NULL;
    UShort              sUsed;
    UChar             * sDestCharPtr;
    SInt                sRet;
    UShort              c;
    UInt                i;
    UShort              wc;

    sDestCharPtr = (UChar *)aDest;

    UTF16BE_TO_WC( wc, aSrc );
    
    if (aDestRemain >= 2)
    {
        if (wc >= 0xc800 && wc < 0xd7b0)
        {
            sSummary = &uhc_2_uni2indx_pagec8[(wc>>4)-0xc80];

            sUsed = sSummary->used;

            i = wc & 0x0f;
            
            if (sUsed & ((UShort) 1 << i))
            {
                /* Keep in `sUsed' only the bits 0..i-1. */
                sUsed &= ((UShort) 1 << i) - 1;

                /*Add `sSummary->indx' and the number of bits set in `sUsed'.*/
                sUsed = (sUsed & 0x5555) + ((sUsed & 0xaaaa) >> 1);
                sUsed = (sUsed & 0x3333) + ((sUsed & 0xcccc) >> 2);
                sUsed = (sUsed & 0x0f0f) + ((sUsed & 0xf0f0) >> 4);
                sUsed = (sUsed & 0x00ff) + (sUsed >> 8);
                sUsed += sSummary->indx;

                c = (uhc_2_2charset_main[sUsed>>6] ^ UHC2_XOR_VALUE) +
                    (uhc_2_2charset[sUsed] ^ UHC2_XOR_VALUE);

                sDestCharPtr[0] = (c >> 8); 
                sDestCharPtr[1] = (c & 0xff);

                sRet = 2;
            }
            else
            {
                sRet = RET_ILUNI;
            }
        }
        else
        {
            sRet = RET_ILUNI;
        }
    }
    else
    {
        sRet = RET_TOOSMALL;
    }

    return sRet;
}
